function paginate(lines: PdfLine[]) {
  const pages: PdfLine[][] = [];
  let current: PdfLine[] = [];
  let y = topY;

  for (const line of lines.flatMap(wrapLine)) {
    const lineHeight = line.size + (line.gapAfter || 5);
    if (y - lineHeight < bottomY && current.length > 0) {
      pages.push(current);
      current = [];
      y = topY;
    }

    current.push(line);
    y -= lineHeight;
  }

  if (current.length > 0) pages.push(current);
  return pages;
}

function renderPage(lines: PdfLine[]) {
  let y = topY;
  const commands = ["0.08 0.10 0.16 rg"];

  for (const line of lines) {
    commands.push(`BT /F1 ${line.size} Tf ${marginX} ${y} Td (${escapePdfText(line.text)}) Tj ET`);
    y -= line.size + (line.gapAfter || 5);
  }

  return commands.join("\n");
}

function writePdf(objects: string[]) {
  const chunks = ["%PDF-1.4\n"];
  const offsets = [0];

  for (let id = 1; id < objects.length; id += 1) {
    offsets[id] = Buffer.byteLength(chunks.join(""), "utf8");
    chunks.push(`${id} 0 obj\n${objects[id]}\nendobj\n`);
  }

  const xrefOffset = Buffer.byteLength(chunks.join(""), "utf8");
  chunks.push(`xref\n0 ${objects.length}\n0000000000 65535 f \n`);

  for (let id = 1; id < objects.length; id += 1) {
    chunks.push(`${String(offsets[id]).padStart(10, "0")} 00000 n \n`);
  }

  chunks.push(`trailer\n<< /Size ${objects.length} /Root 1 0 R >>\nstartxref\n${xrefOffset}\n%%EOF\n`);
  return chunks.join("");
}

function wrapLine(line: PdfLine): PdfLine[] {
  if (!line.text) return [line];

  const maxChars = Math.max(36, Math.floor(contentWidth / (line.size * 0.52)));
  const words = line.text.split(/\s+/);
  const wrapped: PdfLine[] = [];
  let current = "";

  for (const word of words) {
    const candidate = current ? `${current} ${word}` : word;
    if (candidate.length > maxChars && current) {
      wrapped.push({ ...line, text: current, gapAfter: 3 });
      current = word;
    } else {
      current = candidate;
    }
  }

  if (current) wrapped.push({ ...line, text: current });
  return wrapped;
}

function heading(text: string, size: number): PdfLine {
  return { text, size, gapAfter: 10 };
}

function body(text: string): PdfLine {
  return { text, size: 10, gapAfter: 6 };
}

function spacer(): PdfLine {
  return { text: "", size: 6, gapAfter: 8 };
}

function bullets(items: string[]) {
  return items.map((item) => body(`- ${item}`));
}

function numbered(items: string[]) {
  return items.map((item, index) => body(`${index + 1}. ${item}`));
}

function escapePdfText(text: string) {
  return text.replace(/\\/g, "\\\\").replace(/\(/g, "\\(").replace(/\)/g, "\\)");
}
