import { engineName } from "@/lib/resumeEngine";
import { ResumeReport } from "@/lib/report-schema";

type PdfLine = {
  text: string;
  size: number;
  gapAfter?: number;
};

const pageWidth = 612;
const pageHeight = 792;
const marginX = 56;
const topY = 736;
const bottomY = 56;
const contentWidth = pageWidth - marginX * 2;

export function reportToPdf(report: ResumeReport) {
  const lines = buildReportLines(report);
  const pages = paginate(lines);
  const objects: string[] = [];

  objects[1] = "<< /Type /Catalog /Pages 2 0 R >>";
  objects[3] = "<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>";

  const pageObjectIds: number[] = [];

  for (const pageLines of pages) {
    const pageObjectId = objects.length;
    const contentObjectId = pageObjectId + 1;
    pageObjectIds.push(pageObjectId);

    objects[pageObjectId] =
      `<< /Type /Page /Parent 2 0 R /MediaBox [0 0 ${pageWidth} ${pageHeight}] /Resources << /Font << /F1 3 0 R >> >> /Contents ${contentObjectId} 0 R >>`;
    const stream = renderPage(pageLines);
    objects[contentObjectId] = `<< /Length ${Buffer.byteLength(stream, "utf8")} >>\nstream\n${stream}\nendstream`;
  }

  objects[2] = `<< /Type /Pages /Kids [${pageObjectIds.map((id) => `${id} 0 R`).join(" ")}] /Count ${pageObjectIds.length} >>`;

  return Buffer.from(writePdf(objects), "utf8");
}

